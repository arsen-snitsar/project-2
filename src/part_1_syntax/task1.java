package part_1_syntax;

// =========================================================================
// 1. Use variables of different types (int, double, char, boolean, String).
// =========================================================================

public class task1 {
    public static void main(String[] args)
    {
        byte aNewByte = 101;
        // Declaration of a byte variable called "aNewByte" with a value of 101

        short aNewShort = 12023;
        // Declaration of a short variable called "aNewShort" with a value of 12023

        int aNewInt = 1222333444;
        // Declaration of an int variable called "aNewInt" with a value of 1222333444

        long aNewLong = 1222333444555666777L;
        // Declaration of a long variable called "aNewLong" with a value of 1222333444555666777. Used 'L' in the end since Java requires it.

        float aNewFloat = 1.2345f;
        // Declaration of a float variable called "aNewFloat" with a value of 1.2345. Used 'f' in the end since Java requires it.

        double aNewDouble = 1.2233445566778899;
        // Declaration of a double variable called "aNewDouble" with a value of 1.2233445566778899.

        boolean aNewBoolean = true;
        // Declaration of a boolean variable called "aNewBoolean" with a "true" value.

        char aNewChar = 'C';
        // Declaration of a char variable with 'C' character.

        String aNewString = "Hello World";
        // Declaration of a "Hello World" string.
    }
}
